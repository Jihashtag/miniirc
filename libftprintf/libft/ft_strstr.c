#include <string.h>

char	*ft_strstr(char *s1, char *s2)
{
	int	i;
	int	j;

	i = 0;
	if (!s2 || s2[0] == '\0')
		return (s1);
	while (s1[i])
	{
		j = 0;
		while (s2[j] && s1[i + j] == s2[j])
			j++;
		if (s2[j] == '\0')
			return (&s1[i]);
		i++;
	}
	return (NULL);
}
