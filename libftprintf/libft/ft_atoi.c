int		ft_atoi(char *s)
{
	int				ret;
	unsigned int	i;
	int				f;

	f = 0;
	ret = 0;
	i = 0;
	while (s[i] == '\n' || s[i] == ' ' || s[i] == '\v' || s[i] == '\r' \
			|| s[i] == '\f' || s[i] == '\t')
		i++;
	if ((s[i] == '-' && (f = 1)) || s[i] == '+')
		i++;
	while (s && s[i] && s[i] >= '0' && s[i] <= '9')
	{
		ret *= 10;
		ret += s[i] - '0';
		i++;
	}
	if (f)
		return (-ret);
	return (ret);
}
