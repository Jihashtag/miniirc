#include <unistd.h>

int	ft_putendl_fd(char *s, int fd)
{
	int	i;

	i = 0;
	if (!s)
		return (write(1, "(null)\n", 7));
	while (s[i])
		i++;
	write(fd, s, i);
	write(fd, "\n", 1);
	return (i + 1);
}
